            <h2>O que é o colegiado do curso?</h2>

            <br>

            <p>
                De acordo com a Organização Didática da UEPA, deve ser constituído o Colegiado do
                Curso, que é um órgão consultivo e deliberativo que se destina à avaliação da eficiência
                educativa do Projeto Pedagógico do Curso.
            </p>

            <br>

            <h2>Composição do colegiado</h2>

            <br>

            <ul>
                <!--Inicio da lista composição-->
                <li>coordenador(a) do Curso, como presidente;</li>
                <br>
                <li>seis docentes efetivos em exercício;</li>
                <br>
                <li>três discentes do curso, regularmente matriculados;</li>
            </ul>
            <!--Fim da lista-->



            <br>

            <h2>Competências do colegiado do curso</h2>
            <br>
            <ul>
                <!--Inicio lista competencias-->
                <li>
                    implementar no Curso as decisões de cunho acadêmico e científico emanadas do Conselho
                    de Centro do CCNT e do Conselho Universitário;
                </li>

                <br>

                <li>
                    aprovar e encaminhar para análise, ao Conselho de Centro do CCNT, as propostas de
                    alterações para o currículo do Curso;
                </li>

                <br>

                <li>
                    aprovar os programas e ementas das disciplinas do Curso, apresentados pelos
                    departamentos, de acordo com a orientação do Curso;
                </li>

                <br>

                <li>
                    propor planos e projetos de pesquisa e extensão de interesse do Curso, encaminhando-os
                    para análise do Conselho de Centro do CCNT;
                </li>

                <br>

                <li>
                    deliberar, em grau de recurso, sobre as decisões dos professores e do Presidente do
                    Colegiado de Curso;
                </li>

                <br>

                <li>
                    examinar e decidir sobre o aproveitamento de estudos e adaptações;
                </li>

                <br>

                <li>
                    decidir, em grau de recurso, questões relacionadas com a matrícula de alunos
                    transferidos e diplomados, ouvindo o DCA;
                </li>

                <br>

                <li>
                    decidir, em grau de recurso, questões relacionadas com decisões dos coordenadores de
                    Trabalho de Conclusão de Curso.
                </li>

                <br>

                <li>
                    apurar possíveis responsabilidades do Coordenador de Curso pelo não cumprimento da
                    legislação em vigor e propor ao respectivo Conselho sua destituição, por maioria de dois
                    terços de seus membros;
                </li>

                <br>

                <li>
                    apreciar recomendações da Coordenação de Curso sobre assuntos de interesse do Curso;
                </li>

                <br>

                <li>
                    decidir sobre matéria omissa neste Regulamento, no âmbito de sua competência;
                </li>

                <br>

                <li>
                    designar, dentre seus membros, substituto “pró-tempore” para preencher o cargo de
                    coordenador na ausência ou impedimento do mesmo.
                </li>
            </ul>
