                <h2>História do campus <hr></h2>
                
                <p>
                    Dos 20 Campi que compõe a UEPA, 15 estão implantados fora da capital do estado. O Campus XX, o mais recente, desenvolve os cursos: Engenharia de Produção, Licenciatura em Ciências Naturais/Física, Licenciatura Plena em Geografia, Tecnologia de Alimentos,  Tecnologia em Análise e Desenvolvimento de Sistemas, Engenharia de software e Engenharia Florestal e Ambiental.
                </p>
        
                <br>
        
                <p>
                    O Campus XX, inaugurado em maio de 2010, está aliado à concepção contemporânea que vem concorrendo para a substituição do paradigma da “geração do conhecimento para a sociedade” pelo paradigma da “geração do conhecimento com a sociedade”. A emergência desse novo paradigma cobra das academias, além da proximidade física com os diferentes contextos, uma abordagem metodológica própria capaz de renovar a cada dia uma comunidade de interesses e objetivos voltados para o desenvolvimento sustentável.
                </p>

                <br>

                <p>
                    Assim foi pensado e criado o Campus XX que faz convergir a demanda regional com a missão institucional da UEPA e confere ao Município de Castanhal, como centro polarizador, um papel estratégico para as ações de desenvolvimento uma vez que concentra o maior número de Instituições de Ensino Superior – IES – da Região de Integração Guamá, composta pelos Municípios de Castanhal, Colares, Curuçá, Igarapé-Açú, Inhangapí, Magalhães Barata, Maracanã, Marapanim, Santa Izabel do Pará, Santa Maria do Pará, Santo Antônio do Tauá, São Caetano de Odivelas, São Domingos do Capim, São Francisco do Pará, São João da Ponta, São Miguel do Guamá, Terra Alta e Vigia de Nazaré.
                </p>

                <br>

                <h2>Missão do campus <hr></h2>

                <p>
                    Proporcionar economicidade, eficiência, eficácia e efetividade à gestão da Universidade do Estado do Pará, avaliando o cumprimento das metas e comprovando a legalidade e a legitimidade na aplicação dos recursos públicos que administra.
                </p>

                <h2>Visão do campus<hr></h2>

                <p>
                    Ser instrumento de excelência de controle, visando sempre o aperfeiçoamento da gestão dos recursos públicos destinados à Universidade do Estado do Pará.
                </p>
        
                <h2>Estrutura do campus <hr></h2>

                <p>Desde a implantação, no ano de 2010, o Campus dispõe de 6.700m² de área construída
                    distribuída em 3 (três)
                    pavimentos, com espaço de convivência, rampa de acessibilidade e boa incidência de luz natural e circulação
                    de ar, 10 (dez) salas de aula, auditório para 120 lugares, biblioteca, sala de Professores, banheiros
                    masculino e feminino no térreo e no 1º pavimento e instalação hidráulica de combate à incêndio distribuída
                    pelos 3 pavimentos. De um modo geral as salas de aula são amplas, variando de 58m² a 67,8m², o que suporta
                    com folga turmas de 40 alunos.
                </p>

                <br>

                <p> 
                    O Campus está situado às margens de uma via de bom acesso, asfaltada, com circulação de linhas de ônibus
                    urbanas e intermunicipais, visto que a Rua Pedro Porpino que dá acesso ao Campus é um prolongamento da
                    Rodovia PA 320 que liga o Município de Castanhal aos Municípios de São Francisco do Pará, Igarapé-Açu e
                    Maracanã. Observa-se ainda, a rampa de acessibilidade unindo os 3 pavimentos e a amplitude das salas de aula
                    e do auditório. Todos os espaços internos usados para o ensino são refrigerados, bem iluminados e pintados
                    de acordo com as recomendações técnicas sobre ambiência para espaços escolares
                </p>

                <br>

